// Function to get questions for a specific module
export const getModuleQuestions = (category, difficulty = 'beginner') => {
  const key = `${category}_${difficulty}`;
  return moduleQuestions[key] || moduleQuestions.communication_beginner;
};

// Function to get random questions for variety
export const getRandomQuestions = (category, difficulty = 'beginner', count = 5) => {
  const questions = getModuleQuestions(category, difficulty);
  const shuffled = [...questions].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Function to get questions by skill area
export const getQuestionsBySkill = (skillArea, count = 5) => {
  const allQuestions = [];
  
  // Get questions from all difficulties for the skill area
  Object.keys(moduleQuestions).forEach(key => {
    if (key.startsWith(skillArea)) {
      allQuestions.push(...moduleQuestions[key]);
    }
  });
  
  // Shuffle and return requested count
  const shuffled = [...allQuestions].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export default moduleQuestions;
